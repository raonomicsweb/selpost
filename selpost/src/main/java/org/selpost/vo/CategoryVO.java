package org.selpost.vo;

public class CategoryVO {
	
	private String group_codeID;
	private String codeID;
	private String group_codeName;
	private String codeName;
	private int array;
	private String tag_codeID;
	
	
	
	public String getTag_codeID() {
		return tag_codeID;
	}
	public void setTag_codeID(String tag_codeID) {
		this.tag_codeID = tag_codeID;
	}
	public String getGroup_codeID() {
		return group_codeID;
	}
	public void setGroup_codeID(String group_codeID) {
		this.group_codeID = group_codeID;
	}
	public String getCodeID() {
		return codeID;
	}
	public void setCodeID(String codeID) {
		this.codeID = codeID;
	}
	public String getGroup_codeName() {
		return group_codeName;
	}
	public void setGroup_codeName(String group_codeName) {
		this.group_codeName = group_codeName;
	}
	public String getCodeName() {
		return codeName;
	}
	public void setCodeName(String codeName) {
		this.codeName = codeName;
	}
	public int getArray() {
		return array;
	}
	public void setArray(int array) {
		this.array = array;
	}
	
	
}
